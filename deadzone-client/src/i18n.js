function interpolate(template, params = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => params[key] ?? '');
}

export function createTranslator(language) {
  const lang = normalizeLanguage(language);
  return (key, params) => interpolate(ui[lang]?.[key] ?? ui.en[key] ?? key, params);
}

function translateName(group, item, language, fallbackKey = 'name') {
  const lang = normalizeLanguage(language);
  if (!item) return '';
  const translation = entityNames[lang]?.[group]?.[item.id];
  if (typeof translation === 'string') return translation;
  return translation?.[fallbackKey] || item[fallbackKey] || item.name || item.id;
}

export function displayMap(item, language) {
  return item ? { ...item, name: translateName('maps', item, language) } : item;
}

