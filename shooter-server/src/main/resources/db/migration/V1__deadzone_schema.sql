create table if not exists users (
    id bigint generated by default as identity primary key,
    username varchar(32) not null,
    email varchar(120) not null,
    password varchar(255) not null,
    email_verified boolean not null default false,
    email_verified_at timestamptz,
    total_kills integer not null default 0,
    total_assists integer not null default 0,
    total_deaths integer not null default 0,
    wallet integer not null default 0,
    xp integer not null default 0,
    outfit_id varchar(255),
    weapon_id varchar(255),
    weapon_skin_id varchar(255),
    grenade_skin_id varchar(255),
    mission_stats_json varchar(8000),
    admin boolean not null default false,
    version bigint not null default 0
);

create unique index if not exists uk_users_username on users (username);

create table if not exists email_verification_tokens (
    id bigint generated by default as identity primary key,
    token varchar(96) not null,
    user_id bigint not null,
    expires_at timestamptz not null,
    used_at timestamptz
);
create unique index if not exists uk_email_verification_token on email_verification_tokens (token);

create table if not exists lobby_rooms (
    id varchar(16) primary key,
    name varchar(255) not null,
    map_id varchar(32) not null,
    game_mode varchar(32),
    score_limit integer not null default 30,
    time_limit_minutes integer not null default 20,
    players integer not null default 0,
    max_players integer not null default 6,
    blue_players integer not null default 0,
    red_players integer not null default 0,
    allow_bots boolean not null default true,
    permanent boolean not null default false,
    last_activity_at timestamptz,
    version bigint not null default 0
);

create table if not exists friend_requests (
    id bigint generated by default as identity primary key,
    sender_id bigint not null,
    recipient_id bigint not null,
    status varchar(16) not null,
    created_at timestamptz not null
);
create unique index if not exists uk_friend_request_sender_recipient
    on friend_requests (sender_id, recipient_id);
create index if not exists ix_friend_requests_recipient_status
    on friend_requests (recipient_id, status);

create table if not exists friendships (
    id bigint generated by default as identity primary key,
    first_user_id bigint not null,
    second_user_id bigint not null,
    created_at timestamptz not null
);
create unique index if not exists uk_friendship_users on friendships (first_user_id, second_user_id);
create index if not exists ix_friendships_second_user on friendships (second_user_id);

create table if not exists room_invitations (
    id bigint generated by default as identity primary key,
    sender_id bigint not null,
    recipient_id bigint not null,
    room_code varchar(16) not null,
    status varchar(16) not null,
    created_at timestamptz not null,
    expires_at timestamptz not null default (current_timestamp + interval '30 minutes')
);
create index if not exists ix_room_invitations_recipient_status
    on room_invitations (recipient_id, status);

create table if not exists user_owned_outfits (
    user_id bigint not null,
    sort_order integer not null,
    outfit_id varchar(64) not null,
    primary key (user_id, sort_order)
);
create table if not exists user_owned_weapon_skins (
    user_id bigint not null,
    sort_order integer not null,
    skin_id varchar(64) not null,
    primary key (user_id, sort_order)
);
create table if not exists user_owned_grenade_skins (
    user_id bigint not null,
    sort_order integer not null,
    skin_id varchar(64) not null,
    primary key (user_id, sort_order)
);
create table if not exists user_owned_accessories (
    user_id bigint not null,
    sort_order integer not null,
    accessory_id varchar(64) not null,
    primary key (user_id, sort_order)
);
create table if not exists user_equipped_accessories (
    user_id bigint not null,
    sort_order integer not null,
    accessory_id varchar(64) not null,
    primary key (user_id, sort_order)
);
create table if not exists user_weapon_upgrades (
    user_id bigint not null,
    weapon_id varchar(64) not null,
    upgrade_level integer not null,
    primary key (user_id, weapon_id)
);
create table if not exists user_claimed_missions (
    user_id bigint not null,
    sort_order integer not null,
    mission_id varchar(64) not null,
    primary key (user_id, sort_order)
);
create table if not exists user_map_plays (
    user_id bigint not null,
    map_id varchar(64) not null,
    plays integer not null,
    primary key (user_id, map_id)
);
create table if not exists user_weapon_kills (
    user_id bigint not null,
    weapon_id varchar(64) not null,
    kills integer not null,
    primary key (user_id, weapon_id)
);

