delete from email_verification_tokens where user_id not in (select id from users);
delete from friend_requests
where sender_id not in (select id from users) or recipient_id not in (select id from users);
delete from friendships
where first_user_id not in (select id from users) or second_user_id not in (select id from users);
delete from room_invitations
where sender_id not in (select id from users)
   or recipient_id not in (select id from users)
   or room_code not in (select id from lobby_rooms);

